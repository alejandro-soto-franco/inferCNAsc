def parse_simulated_cnvs(adata):
    import re

    parsed = {}

    for cell, entry in adata.obs['simulated_cnvs'].fillna('').items():
        regions = []
        if entry.strip() == '':
            parsed[cell] = []
            continue

        # Split multiple regions
        parts = entry.split(',')
        for region in parts:
            match = re.match(r'(\w+):(\d+)-(\d+) \(CN (\d+)\)', region.strip())
            if match:
                chrom, start, end, cn = match.groups()
                regions.append({
                    'chromosome': chrom,
                    'start': int(start),
                    'end': int(end),
                    'CN': int(cn)
                })
        parsed[cell] = regions

    return parsed

